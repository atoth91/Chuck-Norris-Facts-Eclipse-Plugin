package co.atoth.eclipse.plugin.chucknorris.service;

public class Fact {
    private String id;
    private String text;

    public Fact(String id, String text) {
        this.id = id;
        this.text = text;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}